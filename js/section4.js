$(".section4 .apply1-t").mouseenter(
	function(){
	    $(".section4 .apply1").find(".text").animate({
            'height':'0'
		});
		$(".section4 .apply1").find(".line1").animate({
			'width':'230px'
	    },100);
	    $(".section4 .apply1").find(".line2").animate({
		     'height':'190px'
		},100);
		$(".section4 .apply1").find(".line3").animate({
			'width':'230px'
		},500);
		$(".section4 .apply1").find(".line4").animate({
		     'height':'190px'
		},100);
			
	});
$(".section4 .apply1-t").mouseleave(
	function(){
		$(".section4 .pure").find(".text").animate({
            'height':'222px'
	    });
		$(".section4 .apply1").find(".line1").animate({
		     'width':'0'
		});
		$(".section4 .apply1").find(".line2").animate({
		     'height':'0'
		});
		$(".section4 .apply1").find(".line3").animate({
			'width':'0'
		});
		$(".section4 .apply1").find(".line4").animate({
			'height':'0'
		});
	});
$(".section4 .apply1 .btm").hover(
	function(){
	     $(".section4 .apply1 .btm").find(".bg").animate({
            'top':'0'
	     },0);
    },
    function(){
	     $(".section4 .apply1 .btm").find(".bg").animate({
            'top':'353px'
	    },0);
    });


